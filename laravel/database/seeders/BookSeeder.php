                ->firstOrFail();

            $existingBook = Book::where('isbn', $data['isbn'])->first();
            $product = $existingBook?->product ?? Product::firstOrNew(['name' => $data['name']]);

            $product->fill([
                'name' => $data['name'],
                'type' => 'book',
                'price' => $data['price'],
                'stock_count' => $data['stock_count'],
                'category_id' => $categories[$data['category']]->id,
            ]);
            $product->save();

            $book = Book::updateOrCreate(
                ['product_id' => $product->id],
                [
                    'isbn' => $data['isbn'],
                    'publisher_id' => $publishers[$data['publisher']]->id,
                    'language_id' => $languages[$data['language']]->id,
                    'binding_id' => $bindings[$data['binding']]->id,
                    'year' => $data['year'],
                    'pages_num' => $data['pages_num'],
                    'weight' => $data['weight'],
                    'width' => $data['width'],
                    'height' => $data['height'],
                    'depth' => $data['depth'],
                ]
            );

            $book->authors()->sync([$author->id]);

            ProductImage::where('product_id', $product->id)->delete();

            foreach ($data['images'] ?? [] as $imagePath) {
                ProductImage::create([
                    'product_id' => $product->id,
                    'image_path' => $imagePath,
                ]);
            }
        }
    }
